package santech.model;

public class Komnata {

    String name;
    String dlina;
    String shirina;
    String podacha;
    String sliv;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDlina() {
        return dlina;
    }

    public void setDlina(String dlina) {
        this.dlina = dlina;
    }

    public String getShirina() {
        return shirina;
    }

    public void setShirina(String shirina) {
        this.shirina = shirina;
    }

    public String getPodacha() {
        return podacha;
    }

    public void setPodacha(String podacha) {
        this.podacha = podacha;
    }

    public String getSliv() {
        return sliv;
    }

    public void setSliv(String sliv) {
        this.sliv = sliv;
    }


}
